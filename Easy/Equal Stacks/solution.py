def equalStacks(h1, h2, h3):
    stacks = [h1, h2, h3]
    s = list(map(sum, stacks))
    while len(set(s)) != 1:
        tallest = s.index(max(s))
        first_ele = stacks[tallest].pop(0)
        s[tallest] -= first_ele

    return sum(stacks[0])

