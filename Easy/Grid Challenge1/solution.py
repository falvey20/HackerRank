def gridChallenge(grid):
    rows = [sorted(row) for row in grid]
    
    for i in range(0, len(rows[0])):
        for row in range(0, len(rows)-1):
            if rows[row+1][i] < rows[row][i]:
                return 'NO'
    return 'YES'
            

